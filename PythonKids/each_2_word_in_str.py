s = 'Це якщо не ти дуже це хороший читаєш спосіб то заховати щось повідомлення негаразд'


# В книжці дається такий розв'язок
l = s.split()

for x in range(0, len(l), 2):
    print(l[x])
    
# Але практичніший — наступний, 
# в ньому ми одразу отримуємо ту ж
# стрічку, тільки „розшифровану“.

print(' '.join(l[::2]))

# s — string; l — list
