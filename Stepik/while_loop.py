
i = 0
while i < 5:
    a, b = input().split()
    a = int(a)
    b = int(b)
    if (a == 0) and (b == 0):
        break  # виходимо з цикулу
    if (a == 0) or (b == 0):
        continue
        # переходимо до наступної ітерації, якщо вона є
        # причому переходимо без будь-якого попередження, одразу
    print(a * b)
    i += 1
